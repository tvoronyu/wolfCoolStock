#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char	*str;
	int		i;
	int		n;

	if (s && f)
	{
		i = 0;
		n = ft_strlen((char *)s);
		if (!(str = (char *)malloc(sizeof(char) * (n + 1))))
			return (NULL);
		while (s[i] != 0)
		{
			str[i] = f(i, s[i]);
			i++;
		}
		str[i] = '\0';
		return (str);
	}
	return (NULL);
}
