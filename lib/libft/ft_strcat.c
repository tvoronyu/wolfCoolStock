#include "libft.h"

char	*ft_strcat(char *dest, const char *src)
{
	char *s1;

	s1 = dest;
	while (*dest != '\0')
		dest++;
	while (*src != '\0')
		*dest++ = *src++;
	*dest = '\0';
	return (s1);
}
