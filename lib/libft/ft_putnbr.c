#include "libft.h"

void	ft_putnbr(int nb)
{
	unsigned int	nb2;
	char			s;

	if (nb < 0)
	{
		write(1, "-", 1);
		nb = -nb;
	}
	nb2 = nb;
	if (nb2 >= 10)
	{
		ft_putnbr(nb2 / 10);
		ft_putnbr(nb2 % 10);
	}
	else
	{
		s = nb + '0';
		write(1, &s, 1);
	}
}
