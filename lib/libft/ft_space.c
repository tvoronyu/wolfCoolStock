#include "libft.h"

void	ft_space(const char *str, size_t *i)
{
	int s;

	s = 0;
	while ((str[s] == ' ') || (str[s] == '\t') || (str[s] == '\n')
			|| (str[s] == '\v') || (str[s] == '\f') || (str[s] == '\r'))
		s++;
	*i = s;
}
