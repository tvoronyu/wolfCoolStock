#include "libft.h"

int		ft_strcount(const char *s1, char c)
{
	int n;
	int s;

	n = 0;
	s = 0;
	while (s1[n] != '\0')
	{
		if (s1[n] != c)
		{
			while (s1[n] != '\0' && s1[n] != c)
				n++;
			s++;
		}
		n++;
	}
	return (s);
}
