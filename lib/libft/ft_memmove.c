#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	char *strdst;
	char *strsrc;

	strsrc = (char*)src;
	strdst = (char*)dst;
	if (strsrc < strdst)
	{
		strsrc = strsrc + len - 1;
		strdst = strdst + len - 1;
		while (len > 0)
		{
			*strdst-- = *strsrc--;
			len--;
		}
	}
	else
		while (len > 0)
		{
			*strdst++ = *strsrc++;
			len--;
		}
	return (dst);
}
